                NUTRITION
                <p>Puppies have special nutritional needs to enable them to grow to become healthy adults. 
                Feeding high-quality nutrient-rich puppy food is recommended.</p>
                <p>
                    puppies need about twice as many calories per day than their adult counterparts. 
                    According to the Committee on Nutrient Requirements of Cats and Dogs of the National Academy of Science, a 
                    10-pound puppy that is expected to weigh 33 pounds at adulthood needs to eat about 990 calories per day.
                    In addition, puppies need more protein, fat, vitamins and minerals than adult dogs.
                </p>
                <p>
                A homemade puppy diet should contain the proper balance of:

                Protein, such as chicken, turkey, fish, lean beef
                Carbohydrates, like rice, pasta, potatoes
                Vegetables, such as peas, green beans, carrots
                Fat, often in the form of vegetable oil
                Vitamin/mineral supplements</p>
