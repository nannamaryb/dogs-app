            <p>Puppy (birth until 6-7 months of age)
            During the puppy stage, your dog will go through more physical and mental changes than at any other point in his life. With so much happening so quickly, 
            it is important to focus on the following to ensure you have a well-balanced, friendly and confident adult dog.</p>
            <p>
            Expose your pup to anything he may see or do as an adult. Gradual exposure to as many different types of people, environments and animals as possible is necessary as well as keeping your pup in the house and around family.
            Make sure every experience is a pleasant one as aversive events could have lifelong effects on anxiety, fear and socialization.
            </p>
            <p>
            Start training the day your pup comes home and do not allow your puppy to do anything you do not want them to do as adults. 
            Recognize puppies are learning about their environment and make sure to puppy-proof your home.
            </p>
