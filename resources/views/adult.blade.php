              NUTRITION
              <p>The most important thing to keep in mind when feeding an adult dog is to make sure your dog eats a complete and balanced diet. Start by checking package labels for something called a statement of nutritional adequacy.
                It should say that the food meets nutrient profiles.
                An adult dog diet, or maintenance diet, contains nutrients suited for animals that have passed their growth stage. 
                </p>
              <p>
                Feeding Your Adult Dog
                <br>
                Adult dogs require sufficient nutrients to meet energy needs and to maintain and repair body tissues. 
                The amount you feed your adult dog should be based on his or her size and energy output. 
                Activity levels may vary dramatically between pets, and will play an important role in determining caloric intake.
              </p>
              <p>
                Overweight Dogs
                <br>
                One of the most common pitfalls dog parents should watch out for is overfeeding. Attempts to shower our dogs with love by means of big meals and lots of tasty treats are sweet, but misguided.
                In dogs, as with humans, extra weight can lead to health problems. Be sure to indulge your four-legged friend with affection, not food!
              </p>
              <p>
                the best part about making homemade dog food – just like making anything at home – is that you know exactly what is going into the dog bowl, 
                and not any of that questionable gelatinous gunk from the canned food.
                But it’s important to note that dogs have different nutrient requirements than humans for their proportional body size, such as:
                
                --High-quality protein (meat, seafood, dairy or eggs)
                --Fat (meat or oil)
                --Carbohydrates (grains or vegetables)
                --Calcium (dairy)
                --Essential fatty acids (egg yolks or oatmeal)</p>
